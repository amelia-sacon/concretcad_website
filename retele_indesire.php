<h3> Retele geodezice de indesire </h3>


Reteaua geodezica de indesire este constituita dintr-o serie de puncte, judicios alese, care se determina pe baza retelei geodezice nationale, din aproape in aproape dupa principiul trecerii de le ordin superior la ordin inferior.

Aceasta retea serveste la:
<ul>
 <li>  delimitarea cadastrala a teritoriului administrativ, inclusiv a intravilanelor, prin determinarea pozitiei spatiale (x, y, z) a punctelor de hotar;</li>

<li> reperajul fotogrammetric, in cazul unor ridicari aeriene, respectiv la determinarea punctelor de reper si control necesare redresarii si restitutiei;</li>

<li>   realizarea retelei de ridicare, constituita din ansamblul statiilor de drumuire ale unei ridicari terestre.</li>
</ul>

Conditiile de realizare a retelei geodezice moderne de indesire, sunt urmatoarele:
<ul type="square">
<li>  determinarea integrala sau partiala este de competenta operatorului cadastral, devenind prima si cea mai importanta etapa de lucru pentru intocmirea documentatiei unei unitati administrative; </li>

<li>   baza de plecare o constituie reteaua geodezica nationala GPS, realizata de ONCGC si integrata in cea europeana. Punctele acestei retele de referinta furnizate de Oficiul judetean de cadastru trebuie sa fie suficient de apropiate ca sa permita indesirea ei cu instalatii GPS;</li>

<li>    densitatea punctelor noi trebuie apreciata si asigurata in functie de necesitatile impuse de lucrarile urmatoare, fiind conditionata in esenta de detaliile existente, de instrumentele si metodele folosite la ridicarea acestora;</li>

<li>    realizarea efectiva se face in prezent cu instalatii GPS, in cadrul retelei geodezice moderne preconizate la noi. Procedeul clasic de indesire a retelei prin intersectii este neproductiv, costisitor, de durata si nu mai poate fi luat in considerare decat in situatii de exceptie: prin intersectii inainte se determina punctele dominante, nestationabile (turle de biserici, antene, cosurile fabricilor, etc.), ce vor servi, cu precadere, la orientarea vizelor din punctele cunoscute, sau la retrointersectie.</li>
</ul>

     Recomandarile generale privind realizarea si folosirea retelei de indesire sunt inscrise in normativele si instructiunile tehnice de lucru in vigoare dintre care mentionam:
<ul type="square">
<li>  parametrii de baza ai determinarilor sunt elipsoidul de referinta WGS - 84 (Krasowski), sistemul de proiectie  "Stereograficâ€™70", pentru coordonatele plane si referinta "Marea Neagra 1975" pentru cote;</li>

<li> trecerea coordonatelor din planul secant unic al proiectiei "stereografice 70" intr-un plan secant local in zonele cu deformatii mari, pentru a rezulta astfel suprafetele reale ce intereseaza in cadastru;</li>

<li>  procedeele tehnice de lucru si instrumentele folosite, moderne sau clasice, sunt lasate la alegerea executantului cu conditia asigurarii preciziei corespunzatoare, mentionata in instructiuni;</li>

<li>  alte detalii privind determinarea retelei de indesire sunt date in Normele tehnice pentru introducerea cadastrului general - ONGGC  (2001). In completare se mai folosesc cele pentru intocmirea planurilor la scara 1/2000 - 1/10000 ale Directiei Fond Funciar si Cadastru (1980), Normativul 110-69 al Comitetului pentru Constructii si Sistematizare (1969), ce ramane valabil pana la aparitia unor reglementari noi.</li>
</ul>

    Etapele de lucru in realizarea retelei geodezice de indesire si implicit de sprijin, necesara introducerii cadastrului, sunt:
<ul type="square">
<li>  identificarea si verificarea punctelor retelei geodezice existente in zona;</li>

<li>  proiectarea si materializarea punctelor de indesire necesare;</li>

<li> masuratori in teren si calcule pentru determinarea coordonatelor acestora.</li>
</ul>


