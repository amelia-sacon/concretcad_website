<h3> Topografia inginereasca </h3>


Topografia inginereasca poate fi abordata  sub doua aspecte : 
<ul type="square">
<li> Masurarea elementelor din teren  in vederea transpunerii lor  pe planuri si harti </li>
<li> Transpunerea in teren a proiectelor de executie a diferitelor lucrari de constructii </li>
</ul>
   Topografia inginereasca cuprinde masuratorile care sunt executate pentru proiectarea, executia si exploatarea constructiilor. Lucrarile topografice si geodezice preceda, insotesc si termina orice proces de constructie, contribuind la buna desfasurare a acestuia, atat prin scurtarea termenului de proiectare si executie, cat si printr-o mai buna organizare a locului de munca. Importanta contributiei lucrarilor topografice creste pe masura mecanizarii procesului de constructie precum si a utilizarii tehnicilor si tehnologiilor moderne.
<br/>

 Problemele de baza ale topografiei ingineresti sunt:
<ul>
   <li> Studii tehnico-topografice care constau in realizarea retelei topografice de sprijin si obtinerea documentatiei necesare proiectarii obiectivelor ingineresti </li>
   <li> Proiectarea topo-inginereasca, aceasta cuprinzand:
   <ul>
        <li> realizarea la scari mari a documentatiei topografice necesare proiectarii obiectivelor ingineresti</li>
       <li> pregatirea topografica pentru aplicarea in teren a proiectului
				<ul>
            <li> proiectarea retelei de trasare</li>
            <li>alegerea metodelor de trasare</li>
            <li>alegerea aparatelor folosite la trasare</li>
            <li>calculul elementelor trasarii</li>
				</ul>
		</li>
	</ul></li>
    <li> solutionarea problemelor de sistematizare orizontala si verticala (calculul suprafetelor si volumelor)</li>
    <li> trasarea pe teren a proiectului
		<ul>
        <li>materializarea retelei topografice de trasare</li>
        <li>trasarea axelor si a punctelor principale ale constructiei</li>
        <li>trasarea in detaliu (fundatie, zidarie, puncte de contur, etc.)</li>
		</ul></li>
    <li>trasarea pentru montajul echipamentelor topografice</li>
    <li>observatii topografice care au ca scop urmarirea comportarii in timp a lucrarii (deplasari, deformatii, tasari)</li>
</ul>


